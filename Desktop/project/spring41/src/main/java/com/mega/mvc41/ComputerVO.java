package com.mega.mvc41;

public class ComputerVO {

	String compay;
	String comtype;
	String mousepay;
	String mouse;
	String price;
	
	public String getCompay() {
		return compay;
	}
	public void setCompay(String compay) {
		this.compay = compay;
	}
	public String getComtype() {
		return comtype;
	}
	public void setComtype(String comtype) {
		this.comtype = comtype;
	}
	public String getMousepay() {
		return mousepay;
	}
	public void setMousepay(String mousepay) {
		this.mousepay = mousepay;
	}
	public String getMouse() {
		return mouse;
	}
	public void setMouse(String mouse) {
		this.mouse = mouse;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
	
}
